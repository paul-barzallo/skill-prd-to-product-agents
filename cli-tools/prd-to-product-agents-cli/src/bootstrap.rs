// ── Args ─────────────────────────────────────────────────────────

#[derive(Args)]
pub struct WorkspaceArgs {
    /// Target workspace directory
    #[arg(long)]
    pub target: Option<PathBuf>,
    /// Project name (defaults to target folder name)
    #[arg(long)]
    pub project_name: Option<String>,
    /// GitHub owner
    #[arg(long)]
    pub github_owner: Option<String>,
    /// GitHub repository
    #[arg(long)]
    pub github_repo: Option<String>,
    /// Skip SQLite DB initialization
    #[arg(long)]
    pub skip_db_init: bool,
    /// Skip git commit
    #[arg(long)]
    pub skip_git: bool,
    /// Dry-run mode: list operations without executing
    #[arg(long)]
    pub dry_run: bool,
    /// Preflight only: show dependency status and exit
    #[arg(long)]
    pub preflight_only: bool,
}

#[derive(Args)]
pub struct CommitArgs {
    /// Target workspace directory
    #[arg(long)]
    pub target: Option<PathBuf>,
    /// Commit message
    #[arg(long)]
    pub message: Option<String>,
    /// Allow commits on main branch
    #[arg(long)]
    pub allow_main: bool,
}

// ── Manifest ─────────────────────────────────────────────────────

struct Manifest {
    records: BTreeMap<String, String>,
}

impl Manifest {
    fn new() -> Self {
        Self {
            records: BTreeMap::new(),
        }
    }

    fn set(&mut self, path: &str, kind: &str, ownership: &str, cleanup: &str) {
        let norm = path.replace('\\', "/");
        if norm.is_empty() {
            return;
        }
        self.records
            .insert(norm.clone(), format!("{norm}\t{kind}\t{ownership}\t{cleanup}"));
    }

    fn write(&self, path: &Path) -> Result<()> {
        let mut lines = vec!["# path\tkind\townership\tcleanup_action".to_string()];
        lines.extend(self.records.values().cloned());
        util::write_utf8_lf(path, &(lines.join("\n") + "\n"))
    }
}

