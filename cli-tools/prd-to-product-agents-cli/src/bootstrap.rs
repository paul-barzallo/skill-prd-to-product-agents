// ── Bootstrap workspace ──────────────────────────────────────────

pub fn workspace(skill_root: &Path, args: WorkspaceArgs) -> Result<()> {
    tracing::info!(
        skill_root = %skill_root.display(),
        dry_run = args.dry_run,
        preflight_only = args.preflight_only,
        skip_db_init = args.skip_db_init,
        skip_git = args.skip_git,
        "starting workspace bootstrap"
    );

    let version = util::read_version(skill_root)?;

    // Validate SKILL.md matches VERSION
    let skill_md = skill_root.join("SKILL.md");
    if skill_md.exists() {
        let content = fs::read_to_string(&skill_md)?;
        let pattern = format!("skill-version: {version}");
        if !content.contains(&pattern) {
            bail!("SKILL.md skill-version does not match VERSION ({version})");
        }
    } else {
        bail!("SKILL.md not found at {}", skill_md.display());
    }

    let template_root = skill_root.join("templates").join("workspace");
    if !template_root.is_dir() {
        bail!(
            "Template directory not found: {}",
            template_root.display()
        );
    }

    let target_root = args
        .target
        .as_deref()
        .unwrap_or(Path::new("."));
    let target_root = target_root
        .canonicalize()
        .unwrap_or_else(|_| target_root.to_path_buf());

    // Guard: prevent bootstrapping into the skill's own templates directory
    let template_norm = template_root.to_string_lossy().replace('\\', "/");
    let target_norm = target_root.to_string_lossy().replace('\\', "/");
    if target_norm == template_norm || target_norm.starts_with(&format!("{template_norm}/")) {
        bail!("Target path is inside the skill template directory. Run this targeting a workspace root, not the template folder.");
    }

    let project_name = args
        .project_name
        .unwrap_or_else(|| {
            target_root
                .file_name()
                .map(|n| n.to_string_lossy().to_string())
                .unwrap_or_else(|| "Unnamed Project".to_string())
        });

    // ── Preflight ─────────────────────────────────────────────
    show_preflight(&target_root, &project_name, skill_root);

    if args.preflight_only {
        println!(
            "\n{}",
            "Preflight complete. No files were written."
                .yellow()
        );
        return Ok(());
    }

    // ── Dry run ───────────────────────────────────────────────
    if args.dry_run {
        return dry_run(&template_root, &target_root);
    }
    let binary_bundle_lines = verify_binary_bundle_integrity(skill_root)?;

    // ── Main bootstrap ────────────────────────────────────────
    let overlay_root = target_root.join(".bootstrap-overlays");
    let state_dir = target_root.join(".state");
    let manifest_path = state_dir.join("bootstrap-manifest.txt");
    let report_path = state_dir.join("bootstrap-report.md");

    fs::create_dir_all(&overlay_root)?;
    fs::create_dir_all(&state_dir)?;

    let mut manifest = Manifest::new();
    let mut created: Vec<String> = Vec::new();
    let mut merged: Vec<String> = Vec::new();
    let mut overlays: Vec<String> = Vec::new();
    let mut collisions: Vec<String> = Vec::new();
    let mut touched: Vec<String> = Vec::new();

    // Walk template and copy/merge/overlay
    for entry in WalkDir::new(&template_root)
        .into_iter()
        .filter_map(|e| e.ok())
        .filter(|e| e.file_type().is_file())
    {
        let source = entry.path();
        let relative = util::to_relative_posix(source, &template_root);
        let target_file = target_root.join(relative.replace('/', std::path::MAIN_SEPARATOR_STR));

        if let Some(parent) = target_file.parent() {
            fs::create_dir_all(parent)?;
        }

        if !target_file.exists() {
            // New file: copy
            fs::copy(source, &target_file)?;
            created.push(relative.clone());
            touched.push(relative.clone());
            manifest.set(&relative, "template_copy", "skill", "delete");
        } else if DYNAMIC_GENERATED.contains(&relative.as_str()) {
            // Skip dynamically generated files
            touched.push(relative.clone());
            manifest.set(&relative, "dynamic_generated", "skill", "keep");
        } else {
            // Check for differences
            let source_hash = file_sha256(source)?;
            let target_hash = file_sha256(&target_file)?;

            if source_hash != target_hash {
                if relative == "AGENTS.md" || relative == ".instructions.md" {
                    let block_name = if relative == "AGENTS.md" {
                        "agents-md"
                    } else {
                        "instructions-md"
                    };
                    if merge_managed_block(&target_file, source, block_name)? {
                        touched.push(relative.clone());
                        merged.push(relative.clone());
                        manifest.set(&relative, "managed_merge", "host", "keep");
                        println!("  {} {} (managed block)", "MERGED:".cyan(), relative);
                    }
                } else if relative == ".gitignore" || relative == ".gitattributes" {
                    let new_count = merge_line_entries(&target_file, source)?;
                    if new_count > 0 {
                        touched.push(relative.clone());
                        merged.push(relative.clone());
                        manifest.set(&relative, "line_merge", "host", "keep");
                        println!(
                            "  {} {} (appended {new_count} entries)",
                            "MERGED:".cyan(),
                            relative
                        );
                    }
                } else {
                    // Overlay proposal
                    let overlay_file =
                        overlay_root.join(format!("{}.new", relative.replace('/', std::path::MAIN_SEPARATOR_STR)));
                    if let Some(p) = overlay_file.parent() {
                        fs::create_dir_all(p)?;
                    }
                    fs::copy(source, &overlay_file)?;
                    let ov_rel = util::to_relative_posix(&overlay_file, &target_root);
                    overlays.push(ov_rel.clone());
                    collisions.push(relative.clone());
                    touched.push(ov_rel.clone());
                    manifest.set(&ov_rel, "overlay_copy", "skill", "delete");
                }
            } else {
                manifest.set(&relative, "template_copy", "skill", "delete");
            }
        }
    }

    // ── Token replacement ─────────────────────────────────────
    let github_owner = args.github_owner.as_deref().unwrap_or("");
    let github_repo = args.github_repo.as_deref().unwrap_or("");

    for token_file in TOKEN_FILES {
        let file_path = target_root.join(token_file);
        if file_path.exists() {
            let mut content = fs::read_to_string(&file_path)?;
            let mut changed = false;

            if content.contains("{{PROJECT_NAME}}") {
                content = content.replace("{{PROJECT_NAME}}", &project_name);
                changed = true;
            }
            if !github_owner.is_empty() && content.contains("REPLACE_ME_GITHUB_OWNER") {
                content = content.replace("REPLACE_ME_GITHUB_OWNER", github_owner);
                changed = true;
            }
            if !github_repo.is_empty() && content.contains("REPLACE_ME_GITHUB_REPOSITORY") {
                content = content.replace("REPLACE_ME_GITHUB_REPOSITORY", github_repo);
                changed = true;
            }

            if changed {
                tracing::info!(path = %file_path.display(), "applying transactional token replacement");
                let temp_path = file_path.with_extension("tmp");
                util::write_utf8_lf(&temp_path, &content)?;
                
                let read_back = fs::read_to_string(&temp_path)?;
                if read_back != content {
                    let _ = fs::remove_file(&temp_path);
                    anyhow::bail!("Failed to verify token replacement on {}", file_path.display());
                }
                
                fs::rename(&temp_path, &file_path)?;

                if !touched.contains(&token_file.to_string()) {
                    touched.push(token_file.to_string());
                }
            }
        }
    }

    // ── Run capability refresh ────────────────────────────────
    let caps_path = target_root.join(".github").join("workspace-capabilities.yaml");
    if let Err(e) = write_capabilities_yaml(&target_root, &caps_path) {
        tracing::warn!(error = %e, capabilities_path = %caps_path.display(), "capability detection failed during bootstrap");
        eprintln!("  {} capability detection: {e}", "WARN:".yellow());
    }
    let _ = mark_workspace_bootstrapped(&target_root);

    // ── Write report ──────────────────────────────────────────
    let _backup = util::backup_with_retention(&report_path, 3)?;

    let git_status_str = format!("{}", if util::command_exists("git") { "available" } else { "missing" });
    let sqlite_status_str = format!("{}", if util::command_exists("sqlite3") { "available" } else { "missing" });
    let mdlint_status_str = if util::command_exists("markdownlint") {
        "available".to_string()
    } else {
        "not installed (optional)".to_string()
    };

    let mut report = vec![
        "# Bootstrap Report".to_string(),
        String::new(),
        "## Skill Version".to_string(),
        String::new(),
        format!("<!-- skill-version: {version} -->"),
        format!("- Version: {version}"),
        String::new(),
        "## Preflight".to_string(),
        String::new(),
        format!("- Project Name: {project_name}"),
        format!("- Runner: {}", skill_root.display()),
        format!("- Target: {}", target_root.display()),
        format!("- Git: {git_status_str}"),
        format!("- sqlite3: {sqlite_status_str}"),
        format!("- markdownlint: {mdlint_status_str}"),
        String::new(),
        "## Created Files".to_string(),
    ];

    if created.is_empty() {
        report.push("- None".to_string());
    } else {
        for f in &created {
            report.push(format!("- {f}"));
        }
    }

    report.push(String::new());
    report.push("## Merged Host Files".to_string());
    if merged.is_empty() {
        report.push("- None".to_string());
    } else {
        let mut unique_merged: Vec<_> = merged.clone();
        unique_merged.sort();
        unique_merged.dedup();
        for f in &unique_merged {
            report.push(format!("- {f} (preserved during cleanup)"));
        }
    }

    report.push(String::new());
    report.push("## Overlay Proposals".to_string());
    if overlays.is_empty() {
        report.push("- None".to_string());
    } else {
        for f in &overlays {
            report.push(format!("- {f}"));
        }
    }

    report.push(String::new());
    report.push("## Collisions".to_string());
    if collisions.is_empty() {
        report.push("- None".to_string());
    } else {
        for f in &collisions {
            report.push(format!("- {f}"));
        }
    }

    util::write_utf8_lf(&report_path, &(report.join("\n") + "\n"))?;
    manifest.set(
        ".state/bootstrap-manifest.txt",
        "generated_runtime",
        "skill",
        "delete",
    );
    manifest.set(
        ".state/bootstrap-report.md",
        "generated_runtime",
        "skill",
        "delete",
    );
    manifest.write(&manifest_path)?;

    // ── SQLite init (via sqlite3 CLI) ─────────────────────────
    let mut bootstrap_status = "FULL";
    if !args.skip_db_init {
        if util::command_exists("sqlite3") {
            let db_path = target_root.join(".state").join("project_memory.db");
            if !db_path.exists() {
                let schema_path = target_root.join(".state").join("memory-schema.sql");
                if schema_path.exists() {
                    let status = std::process::Command::new("sqlite3")
                        .arg(db_path.to_string_lossy().as_ref())
                        .arg(format!(".read \"{}\"", schema_path.to_string_lossy().replace('\\', "/")))
                        .status();
                    match status {
                        Ok(s) if s.success() => {
                            tracing::info!(database_path = %db_path.display(), "sqlite database initialized during bootstrap");
                            println!("  {} SQLite DB initialized", "OK:".green());
                        }
                        _ => {
                            tracing::warn!(database_path = %db_path.display(), "sqlite database initialization failed during bootstrap");
                            eprintln!("  {} SQLite DB init failed", "WARN:".yellow());
                            bootstrap_status = "DEGRADED";
                        }
                    }
                }
            }
        } else {
            tracing::warn!("sqlite3 not found; bootstrap database initialization deferred");
            eprintln!(
                "  {} sqlite3 not found -- DB init deferred",
                "WARN:".yellow()
            );
            bootstrap_status = "DEGRADED";
        }
    }

    // Append bootstrap status to report
    let status_section = format!(
        "\n## Bootstrap Status\n\n- Status: {bootstrap_status}{}\n",
        if bootstrap_status == "DEGRADED" {
            "\n- Reason: sqlite3 not available -- DB init deferred\n- Action: Install sqlite3 and run `prdtp-agents-functions-cli database init` manually"
        } else {
            ""
        }
    );
    util::append_utf8_lf(&report_path, &status_section)?;

    // ── Workspace validation ──────────────────────────────────
    let validation_pass = run_workspace_validation(&target_root);
    let governance_status = detect_governance_status(&target_root);
    let readiness_status = detect_readiness_status(&target_root, governance_status);
    let manual_next_steps = collect_manual_next_steps(
        &target_root,
        governance_status,
        github_owner,
        github_repo,
    );

    // ── Git commit ────────────────────────────────────────────
    #[allow(unused_assignments)]
    let mut git_commit_state = String::new();
    if !args.skip_git && util::command_exists("git") {
        let caps_path_check = target_root.join(".github").join("workspace-capabilities.yaml");
        let git_enabled = util::yaml_bool(&caps_path_check, "capabilities.git.policy.enabled", true);

        if git_enabled {
            if has_git_identity(&target_root) {
                // Initialize git if needed
                if !target_root.join(".git").exists() {
                    let _ = std::process::Command::new("git")
                        .args(["-c", "init.defaultBranch=main", "init"])
                        .current_dir(&target_root)
                        .status();
                }
                match run_safe_commit(&target_root, &manifest_path, "chore: bootstrap product-agent workspace", true) {
                    Ok(hash) => {
                        tracing::info!(commit = %hash, "bootstrap git commit created");
                        git_commit_state = hash;
                    }
                    Err(e) => {
                        tracing::warn!(error = %e, "bootstrap git commit failed");
                        eprintln!("  {} git commit: {e}", "WARN:".yellow());
                        git_commit_state = "failed".to_string();
                    }
                }
            } else {
                git_commit_state = "skipped (identity missing)".to_string();
            }
        } else {
            git_commit_state = "skipped (git disabled)".to_string();
        }
    } else if args.skip_git {
        git_commit_state = "skipped".to_string();
    } else {
        git_commit_state = "skipped (git not found)".to_string();
    }

    // ── Git hooks ─────────────────────────────────────────────
    let hooks_installed = if target_root.join(".git").exists() {
        install_pre_commit_hook(&target_root).is_ok()
    } else {
        false
    };

    // ── Append git section to report ──────────────────────────
    let git_section = format!("\n## Git Commit\n\n- Status: {git_commit_state}\n");
    util::append_utf8_lf(&report_path, &git_section)?;
    let binary_bundle_section = format!(
        "\n## Binary Bundle Integrity\n\n- Status: pass\n{}\n",
        binary_bundle_lines
            .iter()
            .map(|line| format!("- {line}"))
            .collect::<Vec<_>>()
            .join("\n")
    );
    util::append_utf8_lf(&report_path, &binary_bundle_section)?;
    let workspace_state_section = format!(
        "\n## Workspace State\n\n- Template State: template\n- Workspace State: bootstrapped\n- Structure validation: {}\n- Governance status: {}\n- Readiness status: {}\n",
        if validation_pass { "pass" } else { "fail" },
        governance_status,
        readiness_status
    );
    util::append_utf8_lf(&report_path, &workspace_state_section)?;
    let manual_steps_section = if manual_next_steps.is_empty() {
        "\n## Manual Next Steps\n\n- None\n".to_string()
    } else {
        let mut section = String::from("\n## Manual Next Steps\n\n");
        for step in &manual_next_steps {
            section.push_str(&format!("- {step}\n"));
        }
        section
    };
    util::append_utf8_lf(&report_path, &manual_steps_section)?;

    // ── Summary ───────────────────────────────────────────────
    let db_exists = target_root
        .join(".state")
        .join("project_memory.db")
        .exists();

    if !validation_pass {
        println!("\n{}", "===================================================".red());
        println!("  {} {bootstrap_status}", "BOOTSTRAP INCOMPLETE -".red());
        println!("{}", "===================================================".red());
    } else {
        println!("\n{}", "===================================================".green());
        println!("  {} {bootstrap_status}", "BOOTSTRAP COMPLETE -".green());
        println!("{}", "===================================================".green());
    }
    println!();
    println!("  Project:      {project_name}");
    println!("  Target:       {}", target_root.display());
    println!();
    println!("  {}", "--- Component Status ---".cyan());
    println!(
        "  Bootstrap:       {}",
        if bootstrap_status == "FULL" {
            bootstrap_status.green().to_string()
        } else {
            bootstrap_status.yellow().to_string()
        }
    );
    println!("  Files created:   {}", format!("{}", created.len()).green());
    let unique_merged_count = {
        let mut m = merged.clone();
        m.sort();
        m.dedup();
        m.len()
    };
    println!("  Host merges:     {unique_merged_count}");
    println!("  Overlays:        {} proposals", overlays.len());
    println!("  Collisions:      {}", collisions.len());
    println!(
        "  Validation:      {}",
        if validation_pass { "PASS".green().to_string() } else { "FAIL".red().to_string() }
    );
    println!("  Governance:      {governance_status}");
    println!("  Readiness:       {readiness_status}");
    println!(
        "  SQLite DB:       {}",
        if db_exists { "initialized".green().to_string() } else { "pending".yellow().to_string() }
    );
    println!("  Git commit:      {git_commit_state}");
    println!(
        "  Git hooks:       {}",
        if hooks_installed { "installed" } else { "not installed" }
    );
    println!();
    println!("  Report:   .state/bootstrap-report.md");
    println!("  Manifest: .state/bootstrap-manifest.txt");
    println!("{}", "===================================================".green());

    tracing::info!(
        target = %target_root.display(),
        created = created.len(),
        merged = unique_merged_count,
        overlays = overlays.len(),
        collisions = collisions.len(),
        validation_pass,
        bootstrap_status,
        "workspace bootstrap completed"
    );

    if !validation_pass {
        std::process::exit(1);
    }
    Ok(())
}

// ── Safe git commit ──────────────────────────────────────────────

pub fn commit(_skill_root: &Path, args: CommitArgs) -> Result<()> {
    let target = args
        .target
        .as_deref()
        .unwrap_or(Path::new("."));
    let target = target.canonicalize().unwrap_or_else(|_| target.to_path_buf());
    let manifest_path = target.join(".state").join("bootstrap-manifest.txt");
    let message = args
        .message
        .unwrap_or_else(|| "chore: bootstrap product-agent workspace".to_string());

    if !manifest_path.exists() {
        bail!(
            "Missing bootstrap manifest: {}",
            manifest_path.display()
        );
    }

    // Check git capability
    let caps_path = target.join(".github").join("workspace-capabilities.yaml");
    if caps_path.exists() && !util::yaml_bool(&caps_path, "capabilities.git.policy.enabled", true) {
        println!(
            "{}",
            "Skipping git commit: Git capability disabled by .github/workspace-capabilities.yaml."
                .yellow()
        );
        return Ok(());
    }

    if !util::command_exists("git") {
        bail!("git is not available");
    }

    let hash = run_safe_commit(&target, &manifest_path, &message, args.allow_main)?;
    if hash.is_empty() {
        println!(
            "{}",
            "No committable bootstrap changes to commit.".yellow()
        );
    } else {
        println!("{} {hash}", "Committed as".green());
    }
    Ok(())
}

