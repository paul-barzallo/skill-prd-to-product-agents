fn has_git_identity(target: &Path) -> bool {
    let name = std::process::Command::new("git")
        .args(["config", "--get", "user.name"])
        .current_dir(target)
        .output()
        .map(|o| !o.stdout.is_empty())
        .unwrap_or(false);
    let email = std::process::Command::new("git")
        .args(["config", "--get", "user.email"])
        .current_dir(target)
        .output()
        .map(|o| !o.stdout.is_empty())
        .unwrap_or(false);
    name && email
}

fn run_safe_commit(target: &Path, manifest_path: &Path, message: &str, allow_main: bool) -> Result<String> {
    if !util::command_exists("git") {
        bail!("git is not available");
    }

    // Initialize git if needed
    if !target.join(".git").exists() {
        std::process::Command::new("git")
            .args(["-c", "init.defaultBranch=main", "init"])
            .current_dir(target)
            .status()?;
    }

    // Branch guard
    if !allow_main {
        let branch = util::command_output("git", &["-C", &target.to_string_lossy(), "branch", "--show-current"])?;
        if branch == "main" {
            bail!("Refusing to commit on 'main'. Use -AllowMain for bootstrap-only commits.");
        }
    }

    // Read manifest lines
    let manifest_content = fs::read_to_string(manifest_path)?;
    let runtime_state = [
        ".state/bootstrap-manifest.txt",
        ".state/bootstrap-report.md",
        ".state/bootstrap-rerun-report.md",
        ".state/sqlite-bootstrap.pending.md",
        ".state/sqlite-bootstrap.report.md",
        ".state/workspace-validation.md",
    ];

    let paths: Vec<String> = manifest_content
        .lines()
        .map(|l| l.trim())
        .filter(|l| !l.is_empty() && !l.starts_with('#'))
        .map(|l| {
            if l.contains('\t') {
                l.split('\t').next().unwrap_or("").trim().to_string()
            } else {
                l.to_string()
            }
        })
        .filter(|p| !p.is_empty())
        .filter(|p| !runtime_state.contains(&p.as_str()))
        .filter(|p| {
            let full = target.join(p.replace('/', std::path::MAIN_SEPARATOR_STR));
            full.exists()
        })
        .collect();

    if paths.is_empty() {
        return Ok(String::new());
    }

    // Check for pre-existing staged files
    let pre_staged = std::process::Command::new("git")
        .args(["diff", "--cached", "--name-only"])
        .current_dir(target)
        .output()?;
    let pre_staged_str = String::from_utf8_lossy(&pre_staged.stdout);
    let foreign: Vec<&str> = pre_staged_str
        .lines()
        .map(|l| l.trim())
        .filter(|l| !l.is_empty() && !paths.contains(&l.to_string()))
        .collect();

    if !foreign.is_empty() {
        bail!(
            "Git index contains staged files outside the bootstrap manifest:\n  {}\nCommit or unstage before bootstrap commit.",
            foreign.join("\n  ")
        );
    }

    // Stage and commit
    for p in &paths {
        let _ = std::process::Command::new("git")
            .args(["add", "--", p])
            .current_dir(target)
            .status();
    }

    let status_check = std::process::Command::new("git")
        .args(["diff", "--cached", "--name-only"])
        .current_dir(target)
        .output()?;
    if status_check.stdout.is_empty() {
        return Ok(String::new());
    }

    std::process::Command::new("git")
        .args(["commit", "-m", message])
        .current_dir(target)
        .status()?;

    let hash = util::command_output("git", &["-C", &target.to_string_lossy(), "rev-parse", "--short", "HEAD"])?;
    Ok(hash)
}

fn install_pre_commit_hook(target: &Path) -> Result<()> {
    let hooks_dir = target.join(".git").join("hooks");
    fs::create_dir_all(&hooks_dir)?;
    let hook_path = hooks_dir.join("pre-commit");
    let hook_content = "#!/bin/sh\nset -eu\nREPO_ROOT=$(git rev-parse --show-toplevel)\nBASE_DIR=\"$REPO_ROOT/.agents/bin/prd-to-product-agents\"\nCLI=\"\"\ncase \"$(uname -s)\" in\n  Darwin) CLI=\"$BASE_DIR/prdtp-agents-functions-cli-darwin-arm64\" ;;\n  Linux) CLI=\"$BASE_DIR/prdtp-agents-functions-cli-linux-x64\" ;;\n  MINGW*|MSYS*|CYGWIN*|Windows_NT) CLI=\"$BASE_DIR/prdtp-agents-functions-cli-windows-x64.exe\" ;;\n  *) : ;;\nesac\nif [ -z \"$CLI\" ] || [ ! -f \"$CLI\" ]; then\n  if [ -f \"$BASE_DIR/prdtp-agents-functions-cli-windows-x64.exe\" ]; then\n    CLI=\"$BASE_DIR/prdtp-agents-functions-cli-windows-x64.exe\"\n  elif [ -f \"$BASE_DIR/prdtp-agents-functions-cli-darwin-arm64\" ]; then\n    CLI=\"$BASE_DIR/prdtp-agents-functions-cli-darwin-arm64\"\n  elif [ -f \"$BASE_DIR/prdtp-agents-functions-cli-linux-x64\" ]; then\n    CLI=\"$BASE_DIR/prdtp-agents-functions-cli-linux-x64\"\n  fi\nfi\nif [ -z \"$CLI\" ] || [ ! -f \"$CLI\" ]; then\n  echo \"[CRITICAL] Pre-commit hook failed: Target CLI binary not found in $BASE_DIR!\" >&2\n  exit 1\nfi\nexec \"$CLI\" --workspace \"$REPO_ROOT\" git pre-commit-validate \"$@\"\n";
    util::write_utf8(hook_path.as_path(), hook_content)?;

    #[cfg(unix)]
    {
        use std::os::unix::fs::PermissionsExt;
        let mut perms = fs::metadata(&hook_path)?.permissions();
        perms.set_mode(0o755);
        fs::set_permissions(&hook_path, perms)?;
    }

    Ok(())
}

fn mark_workspace_bootstrapped(target: &Path) -> Result<()> {
    let governance_path = target.join(".github").join("github-governance.yaml");
    if !governance_path.exists() {
        return Ok(());
    }

    let content = fs::read_to_string(&governance_path)?;
    let updated = content.replace("status: template", "status: bootstrapped");
    if updated != content {
        util::write_utf8_lf(&governance_path, &updated)?;
    }
    Ok(())
}

fn file_has_placeholders(path: &Path) -> bool {
    fs::read_to_string(path)
        .map(|content| {
            content.contains("REPLACE_ME")
                || content.contains("@team-")
                || content.contains("TODO")
                || content.contains("PLACEHOLDER")
                || content.contains("your-org")
                || content.contains("your-repo")
        })
        .unwrap_or(false)
}

fn detect_governance_status(target: &Path) -> &'static str {
    let governance_path = target.join(".github").join("github-governance.yaml");
    let codeowners_path = target.join(".github").join("CODEOWNERS");
    if !governance_path.exists() || !codeowners_path.exists() {
        return "pending_configuration";
    }
    if file_has_placeholders(&governance_path) || file_has_placeholders(&codeowners_path) {
        return "pending_configuration";
    }
    "configured"
}

fn detect_readiness_status(target: &Path, governance_status: &str) -> &'static str {
    if governance_status != "configured" {
        return "not_ready";
    }
    let governance_path = target.join(".github").join("github-governance.yaml");
    if let Ok(content) = fs::read_to_string(governance_path) {
        if content.contains("status: configured") || content.contains("status: production-ready") {
            return "ready";
        }
    }
    "not_ready"
}

