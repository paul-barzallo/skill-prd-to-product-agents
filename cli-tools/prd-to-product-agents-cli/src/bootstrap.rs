// ── Token files ──────────────────────────────────────────────────
const TOKEN_FILES: &[&str] = &[
    "docs/project/vision.md",
    "docs/project/scope.md",
    "docs/project/stakeholders.md",
    ".github/github-governance.yaml",
];

