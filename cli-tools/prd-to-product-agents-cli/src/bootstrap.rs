fn merge_line_entries(target: &Path, source: &Path) -> Result<usize> {
    let existing_content = fs::read_to_string(target)?;
    let template_content = fs::read_to_string(source)?;

    let existing_lines: std::collections::HashSet<String> = existing_content
        .lines()
        .map(|l| l.trim().to_string())
        .filter(|l| !l.is_empty())
        .collect();

    let new_entries: Vec<String> = template_content
        .lines()
        .map(|l| l.trim().to_string())
        .filter(|l| !l.is_empty() && !existing_lines.contains(l))
        .collect();

    if new_entries.is_empty() {
        return Ok(0);
    }

    let label = if target
        .file_name()
        .map_or(false, |n| n == ".gitignore")
    {
        "Bootstrap workspace entries"
    } else {
        "Bootstrap workspace attributes"
    };

    let append = format!("\n# --- {label} ---\n{}\n", new_entries.join("\n"));
    util::append_utf8_lf(target, &append)?;
    Ok(new_entries.len())
}

